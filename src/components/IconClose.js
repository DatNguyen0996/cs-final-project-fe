import React from "react";
import "../style/button.style.css";

function IconClose({ setPopupInfor, setIsOpen, cartId, handleDeleteCart }) {
  const handleCloseBtn = () => {
    if (setPopupInfor !== undefined) setPopupInfor({});
    if (setIsOpen) setIsOpen(false);
    if (handleDeleteCart && cartId) {
      handleDeleteCart(cartId);
    }
  };
  return (
    <div id="iconClose-wrapper" onClick={handleCloseBtn}>
      <img id="iconClose" src="/images/close.png" alt="" />
    </div>
  );
}

export default IconClose;
